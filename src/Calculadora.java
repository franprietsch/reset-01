public class Calculadora {

    int somar (int valor1, int valor2) {
        int resultadoSoma = valor1 + valor2;
        return resultadoSoma;
    }

    int subtrair (int valor1, int valor2) {
        int resultadoSubtracao = valor1 - valor2;
        return resultadoSubtracao;
    }

    int multiplicar (int valor1, int valor2) {
        int resultadoMultiplicao = valor1 * valor2;
        return resultadoMultiplicao;
    }

    int dividir (int valor1, int valor2) {
        int resultadoDivisao = valor1 / valor2;
        return resultadoDivisao;
    }

}
